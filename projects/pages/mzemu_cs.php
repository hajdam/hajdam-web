<h1>Sharp MZ-800 Emulátor</h1>

<p>Několikrát jsem se pokusil vytvořit emulátor pro 8-bitový počítač SHARP MZ-800, typicky jsem pracoval pouze na GUI a samotná emulace vycházela z kódů poskytnutých Zdeňkem Adlerem.</p>

<p>Zatím poslední pokus byl naprogramovat emulátor v jazyce Java.</p>

<p>Stránka: <a class="urlextern" href="https://sharpmz.zdechov.net/?jmzemu">sharpmz.zdechov.net/?jmzemu</a> (TODO)</p>

<p>Zdroje: <a class="urlextern" href="https://github.com/hajdam/jmzemu">github.com/hajdam/jmzemu</a> (TODO)</p>


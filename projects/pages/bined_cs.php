<h1>BinEd Binární/Hexadecimalní Editor</h1>

<p>BinEd je hobby projekt pro vytvoření prohlížeče/editoru pro binární data (hexadecimální editor) v Javě.</p>

<p>Stránka: <a class="urlextern" href="https://bined.exbin.org">bined.exbin.org</a></p>

<p>Zdroje: <a class="urlextern" href="https://github.com/exbin">github.com/exbin</a></p>

<p>BinEd je částečně rozpracovaný hobby projekt. Cíle jsou:</p>

<ul><li>Zdarma a open source (komerčně přátelská licence Apache)</li>
<li>Dostupný jako komponenta / knihovna pro jiné aplikace</li>
<li>Samostatně spustitelný editor</li>
<li>Vytvořit pluginy/rozšíření pro další aplikace (Javová vývojová prostředí atp.)</li></ul>


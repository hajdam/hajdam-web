<h1>Sharp MZ-800 Emulator</h1>

<p>I had multiple attempts on creating emulator for 8-bit computer SHARP MZ-800, typically working on GUI side and less on emulation itself, which was typically provided by Zdeněk Adler.</p>

<p>Last unfinished attempt was tried in Java.</p>

<p>Website: <a class="urlextern" href="https://sharpmz.zdechov.net/?jmzemu">sharpmz.zdechov.net/?jmzemu</a> (TODO)</p>

<p>Sources: <a class="urlextern" href="https://github.com/hajdam/jmzemu">github.com/hajdam/jmzemu</a> (TODO)</p>


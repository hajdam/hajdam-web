<h1>Site: Sharp MZ Wiki</h1>

<p>Site was an attempt to create community driven site for fans of SHARP MZ-800 computers in Czech Republic, but it was not accepted as community is small and they preferred separate personal sites.</p>

<p>Website: <a class="urlextern" href="https://sharpmz.zdechov.net">sharpmz.zdechov.net</a></p>
<p>Sources: <strike>TODO</strike></p>

<center><img src="images/site_sharpmz_en.png" alt="[site_sharpmz_en]" width="700" border="2"/></center>

<p>Goal was to provide wiki capability and editable catalog of files with aggregate download and direct connection to emulator.</p>

<p>Features:</p>
<ul>
<li>Multilanguage site (just English and Czech)</li>
<li>Catalog of files (using MySQL database)</li> 
<li>Integrated DokuWiki engine <a class="urlextern" href="https://dokuwiki.org">dokuwiki.org</a></li>
</ul>

